package be_study.quiz.quiz35;

public class Kichen {

	int size;
	String color;
	int floor;
	
	
	public Kichen(int size, String color, int floor) {
		this.size = size;
		this.color = color;
		this.floor = floor;
	}
	
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getFloor() {
		return floor;
	}
	public void setFloor(int floor) {
		this.floor = floor;
	}
}
