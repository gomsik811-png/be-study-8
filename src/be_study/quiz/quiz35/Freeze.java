package be_study.quiz.quiz35;

public class Freeze {
	
	int whatHall;
	int pirce;
	public Freeze(int whatHall, int pirce) {

		this.whatHall = whatHall;
		this.pirce = pirce;
	}
	public int getWhatHall() {
		return whatHall;
	}
	public void setWhatHall(int whatHall) {
		this.whatHall = whatHall;
	}
	public int getPirce() {
		return pirce;
	}
	public void setPirce(int pirce) {
		this.pirce = pirce;
	}

}
